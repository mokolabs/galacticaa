<p><a href="http://www.amazon.com/exec/obidos/redirect?tag=galacticaanet-20%26link_code=xm2%26camp=2025%26creative=165953%26path=http://www.amazon.com/gp/redirect.html%253fASIN=B000BNI90Y%2526tag=galacticaanet-20%2526lcode=xm2%2526cID=2025%2526ccmID=165953%2526location=/o/ASIN/B000BNI90Y%25253FSubscriptionId=0EMV44A9A5YT1RVDGZ82" title="View product details at Amazon"><img src="/wp-content/images/season2dvd.jpg" alt="Season 2 DVD" style="float: left; padding-right: 10px; padding-bottom: 10px;" /></a></p>
<p>With just a few days left to go before Christmas, Universal has released the <a href="http://www.amazon.com/exec/obidos/redirect?tag=galacticaanet-20%26link_code=xm2%26camp=2025%26creative=165953%26path=http://www.amazon.com/gp/redirect.html%253fASIN=B000BNI90Y%2526tag=galacticaanet-20%2526lcode=xm2%2526cID=2025%2526ccmID=165953%2526location=/o/ASIN/B000BNI90Y%25253FSubscriptionId=0EMV44A9A5YT1RVDGZ82" title="View product details at Amazon">first half of Season 2 on DVD</a>.</p>
<p>The disc contains the following features:</p>
<p>&bull; 3 discs<br />
&bull; 10 episodes<br />
&bull; 440 minutes<br />
&bull; Deleted scenes<br />
&bull; Episode commentaries (from the podcasts)<br />
&bull; Sneak peek at the 2nd half of Season 2<br />
&bull; Dolby Digital 5.1 sound<br />
&bull; Widescreen anamorphic presentation</p>
<p>Notably, though, the DVD does not contain the extended, 90-minute version of the &#8220;Pegasus&#8221; episode where Galactica meets up with another Battlestar which survived the Cylon attack. Current plans call for this episode to be included in the DVD for the 2nd half of DVD&#8230; which will probably appear sometime this summer.</p>
<p>Lastly, I love the fact that Amazon already has <a href="http://www.amazon.com/exec/obidos/redirect?tag=galacticaanet-20%26link_code=xm2%26camp=2025%26creative=165953%26path=http://www.amazon.com/gp/redirect.html%253fASIN=B000BNI90Y%2526tag=galacticaanet-20%2526lcode=xm2%2526cID=2025%2526ccmID=165953%2526location=/o/ASIN/B000BNI90Y%25253FSubscriptionId=0EMV44A9A5YT1RVDGZ82" title="View product details at Amazon">67 reviews</a> for this DVD. That&#8217;s a passionate fanbase for you!</p>
<p>UPDATE: During today&#8217;s marathon, the SciFi channel is promoting the new DVD with commercials that mention a <a href="http://battlestargalacticadvd.com/">special DVD-only website</a>. Not much new there, but there&#8217;s a <a href="http://battlestargalacticadvd.com/trailer2.html">high quality version</a> of the DVD trailer that appeared on the web last week.
</p>

<blockquote><p>NBC will charge 99 cents per episode to access such series as Surface. The news coincides with a similar announcement from CBS and Comcast and follows a widely reported deal between ABC and Apple&#8217;s iPod. </p>
<p>NBC&#8217;s deal with DirecTV, which will offer the programs free of commercials, will cover not only NBC shows such as the Law &#038; Order series, but also a few shows on its cable networks, including USA Network. The NBC-DirecTV programming will be available only to subscribers who purchase the new DirecTV Plus Interactive DVR, which will begin rolling out in stores and online during the next few months. NBC Universal owns SCIFI.COM.</p></blockquote>
<p>There&#8217;s also more in <a href="http://phx.corporate-ir.net/phoenix.zhtml?c=127160&#038;p=irol-newsArticle&#038;ID=779572&#038;highlight=">this press release</a> from DirecTV.

<h2><a href="/2005/11/23/buy-your-very-own-viper-and-cylon-raider" rel="bookmark">Buy your very own Viper and Cylon Raider!</a></h2>
<div class="category">&there4; <strong>November 23, 2005</strong> at 9:59 am | <a href="/categories/galactica-78/" title="View all posts in Galactica '78" rel="category tag">Galactica '78</a>,  <a href="/categories/merchandise/" title="View all posts in Merchandise" rel="category tag">Merchandise</a></div>

<div class="entry">
<p><img src='/wp-content/images/model_auction.jpg' alt='Original Galactica models' /></p>
<p>Just in time for Christmas, &#8216;Profiles in History&#8217; is putting two original Battlestar Galactica models &#8212; one viper, one cylon raider &#8212; <a href="http://cgi.liveauctions.ebay.com/ws/eBayISAPI.dll?ViewItem&#038;category=28265&#038;item=6578148361">up for auction</a> on eBay.</p>
<p>These are the original miniatures created by effects legend John Dykstra and Apogee Effects to film several of the visual effects sequences in the original Galactica television show. If you&#8217;re a fan of the original show or visual effects in general, it&#8217;s definitely cool stuff. (Though, with bidding expected to start at $50,000, few fans will have a chance to participate.)</p>

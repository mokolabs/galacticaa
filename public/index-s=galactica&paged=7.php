<h2><a href="/2005/10/11/the-battlestar-galactica-drinking-game" rel="bookmark">The Battlestar Galactica Drinking Game</a></h2>
<div class="category">&there4; <strong>October 11, 2005</strong> at 9:37 am | <a href="/categories/galactica-78/" title="View all posts in Galactica '78" rel="category tag">Galactica '78</a></div>

<div class="entry">
<p>Just ran across a <a href="http://www.ringthis.com/tv_drinking_games/batlstar.htm">drinking game</a> for the original Battlestar Galactica. If you&#8217;re a fan of the old show like me, you&#8217;ll find this quite hilarious.</p>
<ul>
<li>Whenever a Cylon says &#8220;By your command.&#8221; &#8212; 1 drink</li>
<li>Someone takes the name of &#8220;The Lords of Kobol&#8221; in vain. &#8212; 1 drink</li>
<li>Anyone uses a &#8220;colonialism.&#8221; (I.e., centon, micron, daggit, furlon, cubit, etc.) &#8212; 1 drink</li>
<li>The Council of the Twelve makes a really stupid decision. &#8212; 1 drink</li>
<li>The destruction of a Raider or Viper actually coincides with when the lasers apparently struck it. &#8212; 2 drinks</li>
<li>Starbuck comes up with a cunning plan to get rich quick, only to see it go up in smoke. &#8212; 2 drinks</li>
<li>Rigel says something other than &#8220;Transferring control to all vipers. Launch when ready.&#8221; &#8212; 3 drinks</li>
<li>You actually see Starbuck romancing a woman (Cassiopea does not count) &#8212; 3 drinks</li>
<li>Someone OTHER than Starbuck or Apollo has to make a forced landing on a planet. (We know. It never happens. But, we can always hope.) &#8212; 4 drinks</li>
<li>You actually see one of those battle jackets buttoned. (We dare you to find even one fastener closed.) &#8212; 4 drinks</li>
<li>Someone actually makes a reference to life before the Destruction AFTER the first three episodes (or first, depending on what version you are playing with.) &#8212; 4 drinks</li>
<li>A one-shot character, who allegedly has a long history with one of the main characters, comes out of nowhere, throwing the whole known history of the character in doubt. Take four MORE drinks if the character was believed to be dead. Take ANOTHER five drinks if it was obvious that the one-shot character was created for a &#8220;name&#8221; guest star. Take ANOTHER six drinks if the main character in question is NOT Starbuck. (Warning: Watching &#8220;The Living Legend&#8221; will get you totally smashed under this rule since Commander Cain counts for 18 drinks)</li>
</ul>
<p>There are two <a href="http://www.beaucoupkevin.com/2005/07/beaucoupkevin-presents-completely.html">drinking</a> <a href="http://thecomfychair.blogspot.com/2005/03/battlestar-galactica-drinking-game-hey.html">games</a> for the new show, but both are very short: take a drink when Colonel Tigh drinks. Ha ha!</p>
<p>Pretty funny, but we really need a new version of this game. Comment below with your ideas for a new BSG Drinking Game!
</p>

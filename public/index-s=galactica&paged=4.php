<blockquote><p>From the classic science-fiction series of the late 1970s, we are pleased to present two original screen-used filming miniatures of the main fighter ships, each representing the apex of technology as realized by the Colonials and their arch-enemies, the Cylons. Both of these filming miniatures were created by John Dykstra and the crew of Apogee Effects.</p>

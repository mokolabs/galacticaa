<h2><a href="/2005/10/14/the-return-of-ron-moore" rel="bookmark">The Return of Ron Moore</a></h2>
<div class="category">&there4; <strong>October 14, 2005</strong> at 5:16 pm | <a href="/categories/battlestar-news/" title="View all posts in Battlestar News" rel="category tag">Battlestar News</a>,  <a href="/categories/cast-crew/" title="View all posts in Cast &#038; Crew" rel="category tag">Cast &#038; Crew</a></div>

<div class="entry">
<p>Ron Moore has just updated his <a href="http://blog.scifi.com/battlestar/">long-dormant blog</a> on the SciFi Channel website:</p>
<blockquote><p>Today is something of a notable day, in that we&#8217;re putting out first drafts on the final three episodes of Season Two, which means that we&#8217;re now officially in the endgame. There&#8217;s a sense of relief, coupled with sadness, now that the last first drafts are being published, because it means that the heavy-lifting (in terms of writing) has come to a close, and that the final stories have been laid before us. When I was at Trek, and doing 26 hours a year, the end of the season was more an occasion for the bleary-eyed and nearly brain-dead staff to plan their escapes from the Hart Building and grab a few blissful days of drunken vacation before returning to plan out the next season than it had a taste of the bittersweet. With Galactica, while the end does bring with it the happy thoughts of not having to start with a blank page again this year and the reward of time spent thinking about something besides Kara&#8217;s missing ovaries (&#8221;Now, where did I leave those ovaries? They were right here a minute ago&#8230;&#8221;) it&#8217;s also a reminder of just how fast this trip is going. Two seasons and a miniseries nearly under our belt at this point. How is that possible? I can&#8217;t decide if I&#8217;m just getting old or if the special quality of this experience is truly making it all seem like it&#8217;s roaring past me at an impossibly fast pace.</p></blockquote>
<p>Other interesting tidbits: Moore talks about the original ending of &#8216;Home: Part II&#8217;, screwing up something really bad in an episode (he doesn&#8217;t say what), and avoiding Star Trek&#8217;s tendency toward technobabble.</p>
<p><strong>SciFi:</strong> <a href="http://blog.scifi.com/battlestar/">Battlestar Galactica Blog</a>

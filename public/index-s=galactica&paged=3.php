<h2><a href="/2005/12/16/frak-no-extended-pegasus-episode-on-season-2-dvd" rel="bookmark">Frak! No extended &#8220;Pegasus&#8221; episode on Season 2 DVD!</a></h2>
<div class="category">&there4; <strong>December 16, 2005</strong> at 7:56 pm | <a href="/categories/battlestar-news/" title="View all posts in Battlestar News" rel="category tag">Battlestar News</a>,  <a href="/categories/merchandise/" title="View all posts in Merchandise" rel="category tag">Merchandise</a></div>

<div class="entry">
<p>According to a <a href="http://www.tvshowsondvd.com/newsitem.cfm?NewsID=4712">report from TV Shows on DVD</a>, the Season 2 DVD will NOT contain the extended cut of the &#8220;Pegasus&#8221; episode.</p>
<blockquote><p>Fans of Battlestar Galactica were excited when Ron Moore talked about an extended episode of &#8220;Pegasus,&#8221; the half-season finale, during his PodCast for the show. Moore said that the extended episode would be released on DVD, and it was assumed that it would be on the 2.0 set. Reviewers, and some fans, have already obtained the 2.0 set (which streets on Tuesday, 12/20), but the extended episode wasn&#8217;t included. Message boards are filled with fuming fans, but we have good news that should extinguish the flames.</p>
<p>I spoke with a contact at Universal yesterday and asked about the extended episode. I was told that they couldn&#8217;t ready the episode in time for the set, but it would be the first episode in the next set (the second half of season 2). This sounds like a great idea since fans will own both versions of the episode, unlike the SG-1 extended episode issue. MGM released the shorter cut of &#8220;Threads&#8221; in the season 8 set, and fans wishing to obtain the longer version must send in their current disc, or a proof of purchase for the set. It&#8217;s more complicated than Universal&#8217;s solution.</p></blockquote>
<p>This is definitely disappointing. While I&#8217;m glad there will still be an opportunity to view the extended cut, it sucks we now have to wait another 6 months or so to see it. (Though, on the other hand, when the 2nd Season 2 DVD does show up, it will be a much-needed antidote to Galactica withdrawl syndrome as we wait for Season 3 to begin.)</p>
<p>(Thanks to Justine at <a href="http://tahmohpenikett.blogspot.com/">All about Tahmoh Penikett</a> for bringing this sad news to our attention.)
</p>

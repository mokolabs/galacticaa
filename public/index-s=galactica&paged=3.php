<h2><a href="/2005/12/29/galacticaa-gets-an-upgrade" rel="bookmark">Galacticaa gets an upgrade</a></h2>
<div class="category">&there4; <strong>December 29, 2005</strong> at 10:23 am | <a href="/categories/site-news/" title="View all posts in Site News" rel="category tag">Site News</a></div>

<div class="entry">
<p>In advance of next week&#8217;s new Galactica episode, we&#8217;ve made a number of improvements to Galacticaa:</p>
<p>&bull; Massively reworked our tags implementation &#8212; the <a href="/tags">main tag cloud</a> now shows all available tags, the mini tag cloud in the sidebar has more tags and a new design, the mini tag cloud is also higher up in the sidebar so it&#8217;s easier to see, and Technorati tag links now include a Technorati logo. Also &#8212; tags like &#8216;bsg&#8217; and &#8216;galactica&#8217; (which are great for search engines, but not useful when browsing this site) are now hidden from view.</p>
<p>&bull; Unified the appearance of the index, category, archive, and search results pages. (Previously, there were slight layout differences between all of these pages.)</p>
<p>&bull; Added a search box to the <a href="/index.php?s=galactica">search results page</a> so it&#8217;s easier to refine search queries</p>
<p>&bull; Improved the &#8216;Who&#8217;s Online?&#8217; counter script &#8212; it should now both a list of logged-in users and a count of all guests</p>
<p>&bull; Removed the Google Adsense banners. (For now, the income wasn&#8217;t worth the trouble&#8230; they may return in the future.)</p>
<p>&bull; Reduced the number of del.icio.us links to five. (Larger numbers of links tend to contain duplicates, and a larger list refreshes less often for repeat visitors.)</p>
<p>&bull; Reorganized the Galactica links in the sidebar. (Removed dead links, renamed link groups, and renamed a few links so that they&#8217;re more accurate.)</p>
<p>&bull; Tweaked the site footer a little bit</p>
<p>(Don&#8217;t worry, though. We didn&#8217;t network our computers.)
</p>

<p><img src="/wp-content/images/itms.jpg" alt="iTunes Music Store... now with BSG!" /></p>
<p>Apple <a href="http://www.apple.com/pr/library/2005/dec/06nbc.html">has just announced a deal with NBC/Universal</a> to offer several television shows for sales on the iTunes Music Store. So, you know what that means&#8230; Apple&#8217;s new iPods can now play episodes of Battlestar Galactica!</p>
